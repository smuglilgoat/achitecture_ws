def increment_third_element(sets):
    result = set()
    
    for subset in sets:
        # Convert each subset to a list to modify it
        subset_list = list(subset)
        
        if len(subset_list) >= 3:
            # Increment the third element
            subset_list[2] += 1
        
        # Convert the modified list back to a set
        result.add(tuple(subset_list))  # Tuples are used to maintain order and immutability in sets
    
    return result

